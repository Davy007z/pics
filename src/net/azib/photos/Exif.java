package net.azib.photos;

public class Exif {
  Float fstop;
  Float exposure;
  Float focal;
  String iso;
  String camera;

  public Float getFstop() {
    return fstop;
  }

  public Float getExposure() {
    return exposure;
  }

  public Float getFocal() {
    return focal;
  }

  public String getIso() {
    return iso;
  }

  public String getCamera() {
    return camera;
  }
}
