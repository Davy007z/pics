package net.azib.photos;

public class Comment {
  String author;
  String avatarUrl;
  String authorId;
  String text;
  String photoId;

  public String getAuthor() {
    return author;
  }

  public String getAvatarUrl() {
    return avatarUrl;
  }

  public String getAuthorId() {
    return authorId;
  }

  public String getText() {
    return text;
  }

  public String getPhotoId() {
    return photoId;
  }
}
